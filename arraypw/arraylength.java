public class arraylength {
    public static void main(String[] args) {
        int nums[][]={
            {3,9,7,5},
            {1,5,6,5},
            {8,4,5,6}
        };
        System.out.println(nums.length);// the answer will be 3 because each row represents 1 element
        System.out.println(nums[0].length);// array length of first element
    }
}
